class Profile {
  int id;
  String avatar;
  String name;
  String username;
  String email;
  String post;
  String phone;
  String dob;
  String gender;
  String address;
  String bankName;
  String bankNumber;
  String joinedDate;
  String employeeCode;
  String department;
  String branch;

  Profile(
      {required this.id,
      required this.avatar,
      required this.name,
      required this.username,
      required this.email,
      required this.post,
      required this.phone,
      required this.dob,
      required this.gender,
      required this.address,
      required this.bankName,
      required this.bankNumber,
      required this.joinedDate,
      required this.employeeCode,
      required this.department,
      required this.branch});
}
